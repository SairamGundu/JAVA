class Multiplication {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;
        int res = a * b;
        System.out.println("The multiplication of " + a + " and " + b + " is: " + res);

        a = 200;
        b = 127;
        res = a * b;
        System.out.println(res);

        System.out.println(10 * 'a');
        System.out.println(10 - 20 + 30 * 5 + 6 - 5);

    }

}
