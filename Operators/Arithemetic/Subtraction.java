class Subtraction {
    public static void main(String[] args) {
        int a = 10, b = 5;
        int res = a - b;
        System.out.println(res);

        a = -20;
        b = 30;
        res = a - b;
        System.out.println(res);

        a = 4;
        b = 6;
        res = a - b;
        System.out.println(res);

        System.out.println('b' - 10);
    }
}