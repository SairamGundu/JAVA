package Encapsulation;

public class Atm {

	private int id;
	private String location;
	private double cash;
	
	//getter for id;
	public int getId() {
		return id;
	}
	
	//Setter for id;
	public void setId(int id) {
		this.id=id;
	}

	//getter for location;
		public String getLocation() {
			return location;
		}
		
		//Setter for id;
		public void setLocation(String location) {
			this.location=location;
		}

		//getter for id;
		public double getCash() {
			return cash;
		}
		
		//Setter for id;
		public void setCash(double cash) {
			this.cash=cash;
		}

}
